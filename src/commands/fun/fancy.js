module.exports = {
  // ═══════════════════════════════════════
  // CHANGER LA POLICE D'UN TEXTE (FANCY)
  // ═══════════════════════════════════════
  fancy: {
    desc: 'Convertir du texte en différentes polices fancy',
    category: 'fun',
    usage: '.fancy <texte> ou .fancy <1-10> <texte>',
    async handler(sock, msg, { args, reply }) {
      try {
        if (!args.length) return reply(`❌ Usage: .fancy <texte> ou .fancy <numéro> <texte>\n\nPolices disponibles: 1 à 12`)

        let style = null
        let text = args.join(' ')

        if (!isNaN(args[0]) && parseInt(args[0]) >= 1 && parseInt(args[0]) <= 12) {
          style = parseInt(args[0])
          text = args.slice(1).join(' ')
        }

        if (!text) return reply('❌ Donne un texte à convertir.')

        const fonts = {
          1: { name: '𝗚𝗿𝗮𝘀', fn: t => toUnicode(t, 'bold') },
          2: { name: '𝘐𝘵𝘢𝘭𝘪𝘲𝘶𝘦', fn: t => toUnicode(t, 'italic') },
          3: { name: '𝙂𝙧𝙖𝙨 𝙄𝙩𝙖𝙡𝙞𝙦𝙪𝙚', fn: t => toUnicode(t, 'bold_italic') },
          4: { name: '𝔊𝔬𝔱𝔥𝔦𝔮𝔲𝔢', fn: t => toUnicode(t, 'fraktur') },
          5: { name: '𝕯𝖔𝖚𝖇𝖑𝖊', fn: t => toUnicode(t, 'fraktur_bold') },
          6: { name: '𝕮𝕷𝕺𝕾𝕰𝕯', fn: t => toUnicode(t, 'double_struck') },
          7: { name: '𝒮𝒸𝓇𝒾𝓅𝓉', fn: t => toUnicode(t, 'script') },
          8: { name: '𝓢𝓬𝓻𝓲𝓹𝓽 𝓑𝓸𝓵𝓭', fn: t => toUnicode(t, 'script_bold') },
          9: { name: 'ᴍᴏɴᴏ', fn: t => toUnicode(t, 'mono') },
          10: { name: 'ꜱᴀɴꜱ', fn: t => toUnicode(t, 'sans') },
          11: { name: '𝖲𝖺𝗇𝗌 𝖦𝗋𝖺𝗌', fn: t => toUnicode(t, 'sans_bold') },
          12: { name: '🅒🅘🅡🅒🅛🅔', fn: t => toCircled(t) },
        }

        if (style) {
          const f = fonts[style]
          if (!f) return reply('❌ Numéro de police invalide (1-12)')
          return reply(`*${f.name}*\n${f.fn(text)}`)
        }

        // Afficher toutes les polices
        let result = `✨ *FANCY TEXT - "${text}"*\n\n`
        for (const [num, f] of Object.entries(fonts)) {
          result += `*${num}.* ${f.name}\n${f.fn(text)}\n\n`
        }
        result += `Tape *.fancy <numéro> ${text}* pour envoyer une police spécifique.`
        reply(result)
      } catch (e) {
        reply('❌ Erreur: ' + e.message)
      }
    }
  }
}

